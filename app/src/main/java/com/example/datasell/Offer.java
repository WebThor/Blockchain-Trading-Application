package com.example.datasell;

public class Offer {
    private String ownerAddress;
    private String contractAddress;
    private String addressbookAddress;
    private int isOffer;
    private String metaData;
    private String agreGroup;
    private String gender;
    private String education;
    private String fromData;
    private String toDate;
    private String price;
    private String typeOfData;
    private String gateKeeper;


    public Offer(){

    }

    public String getOwnerAddress() {
        return ownerAddress;
    }

    public void setOwnerAddress(String ownerAddress) {
        this.ownerAddress = ownerAddress;
    }

    public String getContractAddress() {
        return contractAddress;
    }

    public void setContractAddress(String contractAddress) {
        this.contractAddress = contractAddress;
    }

    public String getAddressbookAddress() {
        return addressbookAddress;
    }

    public void setAddressbookAddress(String addressbookAddress) {
        this.addressbookAddress = addressbookAddress;
    }

    public int getIsOffer() {
        return isOffer;
    }

    public void setIsOffer(int isOffer) {
        this.isOffer = isOffer;
    }

    public String getMetaData() {
        return metaData;
    }

    public void setMetaData(String metaData) {
        this.metaData = metaData;
    }

    public String getAgreGroup() {
        return agreGroup;
    }

    public void setAgreGroup(String agreGroup) {
        this.agreGroup = agreGroup;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getEducation() {
        return education;
    }

    public void setEducation(String education) {
        this.education = education;
    }

    public String getFromData() {
        return fromData;
    }

    public void setFromData(String fromData) {
        this.fromData = fromData;
    }

    public String getToDate() {
        return toDate;
    }

    public void setToDate(String toDate) {
        this.toDate = toDate;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getTypeOfData() {
        return typeOfData;
    }

    public void setTypeOfData(String typeOfData) {
        this.typeOfData = typeOfData;
    }

    public String getGateKeeper() {
        return gateKeeper;
    }

    public void setGateKeeper(String gateKeeper) {
        this.gateKeeper = gateKeeper;
    }





}
